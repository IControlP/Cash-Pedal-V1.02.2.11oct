# Helper functions for accessing the database
def get_manufacturers_h():
    """Get all manufacturers starting with 'H'"""
    return list(MANUFACTURERS_H.keys())

def get_models_for_make_h(make):
    """Get all models for a specific make starting with 'H'"""
    return list(MANUFACTURERS_H.get(make, {}).keys())

def get_production_years_h(make, model):
    """Get production years for a specific make/model starting with 'H'"""
    model_data = MANUFACTURERS_H.get(make, {}).get(model, {})
    production_years = model_data.get('production_years', (2000, 2025))
    
    # Handle special cases for Honda models with production gaps
    if make == 'Honda':
        if model == 'Ridgeline':
            # Honda Ridgeline had a gap: 2006-2014, then 2017-2025
            return production_years  # Tuple shows the complex pattern
        elif model == 'Passport':
            # Honda Passport had a gap: 1994-2002, then 2019-2025  
            return production_years
        elif model == 'Insight':
            # Honda Insight had gaps: 1999-2006, 2010-2014, 2019-2022
            return production_years
    
    return production_years

def get_trims_for_year_h(make, model, year):
    """Get available trims and prices for a specific year"""
    model_data = MANUFACTURERS_H.get(make, {}).get(model, {})
    trims_by_year = model_data.get('trims_by_year', {})
    
    # Return exact year if available
    if year in trims_by_year:
        return trims_by_year[year]
    
    # Find closest year if exact year not available
    available_years = sorted(trims_by_year.keys())
    if not available_years:
        return {}
    
    # Find closest year within production range
    production_start, production_end = get_production_years_h(make, model)
    
    # Handle complex production patterns
    if isinstance(production_start, tuple):
        # Complex production pattern - check if year falls within any production run
        year_available = False
        if make == 'Honda' and model == 'Ridgeline':
            year_available = (2006 <= year <= 2014) or (2017 <= year <= 2025)
        elif make == 'Honda' and model == 'Passport':
            year_available = (1994 <= year <= 2002) or (2019 <= year <= 2025)
        elif make == 'Honda' and model == 'Insight':
            year_available = (1999 <= year <= 2006) or (2010 <= year <= 2014) or (2019 <= year <= 2022)
        elif make == 'Hyundai' and model == 'Genesis':
            year_available = (2009 <= year <= 2016)
        
        if not year_available:
            return {}
    else:
        if year < production_start or year > production_end:
            return {}
    
    # Find closest available year
    closest_year = min(available_years, key=lambda x: abs(x - year))
    return trims_by_year.get(closest_year, {})

def is_model_available_in_year_h(make, model, year):
    """Check if a model was available in a specific year"""
    model_data = MANUFACTURERS_H.get(make, {}).get(model, {})
    production_years = model_data.get('production_years', (2000, 2025))
    
    # Handle special cases with production gaps
    if make == 'Honda':
        if model == 'Ridgeline':
            return (2006 <= year <= 2014) or (2017 <= year <= 2025)
        elif model == 'Passport':
            return (1994 <= year <= 2002) or (2019 <= year <= 2025)
        elif model == 'Insight':
            return (1999 <= year <= 2006) or (2010 <= year <= 2014) or (2019 <= year <= 2022)
    elif make == 'Hyundai':
        if model == 'Genesis':
            return (2009 <= year <= 2016)
        elif model == 'Ioniq':
            return (2017 <= year <= 2022)
    
    # Standard production range check
    if isinstance(production_years[0], tuple):
        # Multiple production runs - this shouldn't happen with current data structure
        return False
    
    start_year, end_year = production_years
    return start_year <= year <= end_year

def get_base_price_h(make, model, year):
    """Get the base (lowest) price for a model in a specific year"""
    trims = get_trims_for_year_h(make, model, year)
    return min(trims.values()) if trims else 0

def get_trim_price_h(make, model, trim, year):
    """Get price for a specific trim in a specific year"""
    trims = get_trims_for_year_h(make, model, year)
    return trims.get(trim, 0)

