# Helper functions for accessing the database
def get_manufacturers_l():
    """Get all manufacturers starting with 'L'"""
    return list(MANUFACTURERS_L.keys())

def get_models_for_make_l(make):
    """Get all models for a specific make starting with 'L'"""
    return list(MANUFACTURERS_L.get(make, {}).keys())

def get_production_years_l(make, model):
    """Get production years for a specific make/model starting with 'L'"""
    model_data = MANUFACTURERS_L.get(make, {}).get(model, {})
    production_years = model_data.get('production_years', (2000, 2025))
    
    # Handle special case for Lincoln Aviator (two production runs)
    if isinstance(production_years[0], tuple):
        # For models with multiple production runs, return the overall range
        all_years = []
        for run in production_years:
            if len(run) == 2:
                all_years.extend(range(run[0], run[1] + 1))
            else:
                all_years.extend(run)
        return (min(all_years), max(all_years))
    
    return production_years

def get_trims_for_year_l(make, model, year):
    """Get available trims and prices for a specific year"""
    model_data = MANUFACTURERS_L.get(make, {}).get(model, {})
    trims_by_year = model_data.get('trims_by_year', {})
    
    # Return exact year if available
    if year in trims_by_year:
        return trims_by_year[year]
    
    # Find closest year if exact year not available
    available_years = sorted(trims_by_year.keys())
    if not available_years:
        return {}
    
    # Find closest year within production range
    production_start, production_end = get_production_years_l(make, model)
    if year < production_start:
        return trims_by_year.get(production_start, {})
    elif year > production_end:
        return trims_by_year.get(production_end, {})
    else:
        # Find closest available year
        closest_year = min(available_years, key=lambda x: abs(x - year))
        return trims_by_year.get(closest_year, {})

def is_model_available_in_year_l(make, model, year):
    """Check if a model was available in a specific year"""
    model_data = MANUFACTURERS_L.get(make, {}).get(model, {})
    production_years = model_data.get('production_years', (2000, 2025))
    
    # Handle special case for Lincoln Aviator (two production runs)
    if make == 'Lincoln' and model == 'Aviator':
        return (2003 <= year <= 2005) or (2020 <= year <= 2025)
    
    start_year, end_year = production_years
    return start_year <= year <= end_year

def get_base_price_l(make, model, year):
    """Get the base (lowest) price for a model in a specific year"""
    trims = get_trims_for_year_l(make, model, year)
    return min(trims.values()) if trims else 0

def get_trim_price_l(make, model, trim, year):
    """Get price for a specific trim in a specific year"""
    trims = get_trims_for_year_l(make, model, year)
    return trims.get(trim, 0)

